public class ExpressoesComparativas {
    public static void main(String[] args) {
        int bread = 1;
        int sugar = 2;

        System.out.println(bread == sugar);
        System.out.println(bread != sugar);
        System.out.println(bread > sugar);
        System.out.println(bread < sugar);
        System.out.println(bread >= sugar);
        System.out.println(bread <= sugar);

    }
}
